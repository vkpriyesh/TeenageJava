#include<stdio.h>
#include<math.h>
void main(){
float ox,oy,cx,cy,ar,r1,r2,r,per;
  printf("Center coordinates of circle:");
  scanf("%f%f",&ox,&oy);
  printf("Any coordinates on the circumference of circle:");
  scanf("%f%f",&cx,&cy);
  r1=pow((cx-ox),2);
  r2=pow((cy-oy),2);
  r=sqrt(r1+r2);
  ar=3.14*r*r;
  per=2*3.14*r;
  printf("The area of the circle with coordinates of center as (%f,%f) is:%f sq. units.\n",ox,oy,ar);
  printf("The perimeter of the circle with coordinates of center as (%f,%f) is:%f units.\n",ox,oy,per);
}


